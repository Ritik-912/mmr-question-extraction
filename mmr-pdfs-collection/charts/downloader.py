excepted_pdfs = []

def download_pdf(url: str, save_path: str) -> None:
    headers = {'User-Agent': choice(userAgentArray)}
    try:
        response = get(url, headers=headers)
        with open(save_path, 'wb') as file:
            file.write(response.content)
    except:
        excepted_pdfs.append({"pdf_url": url, "save_path": save_path})

def save_path(url: str) -> str:
    name = "../../pdfs/charts/" + url.split('/')[4]
    makedirs(name, exist_ok=True)
    name += "/" + "_".join(url.split('/')[-2:]).split('?')[0] + ".pdf"
    return name

for pdf_url, viewer_url in tqdm(read_csv("links.csv").values.tolist()):
    download_pdf(url = pdf_url, save_path = save_path(url = viewer_url))

